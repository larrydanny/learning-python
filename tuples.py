# tuples are immutable. We can't mutable or change them
# We can't add new item, remove item or modify item.
# To define tuples use parenthesis ()
numbers = (1, 2, 3, 2)

print("print index 2 value")
print(numbers[2])

print("Count the tuple")
print(numbers.count(2))

print("Get index of item 2")
print(numbers.index(2))
