course = "Python for Beginners"
print(len(course))  # len() is a built-in function
print(course.upper())  # upper() is a method of course. convert all char UPPERCASE
print(course.lower())  # lower() is a method of course. convert all char lowercase
print(course.find('P'))  # find() is a method of course. return index of given char
print(course.replace('Beginners', "Medium"))  # replace() is a method of course. replace first one to second
print('Beginners' in course)  # check the char have in course or not. It's return boolean
